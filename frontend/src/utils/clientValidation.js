export const validateClientForm = (formData, isEdit = false) => {
  const errors = {};

  // Company Name
  if (!formData.companyName || !formData.companyName.trim()) {
    errors.companyName = 'Company name is required';
  } else if (formData.companyName.trim().length > 50) {
    errors.companyName = 'Company name must be 50 characters or less';
  }

  // Address
  if (formData.address && formData.address.trim().length > 100) {
    errors.address = 'Address must be 100 characters or less';
  }

  // Email
  if (!formData.email || !formData.email.trim()) {
    errors.email = 'Email is required';
  } else if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(formData.email)) {
    errors.email = 'Invalid email address';
  }

  // Contacts
  const validContacts = (formData.contacts || []).filter((c) => c.name?.trim());
  if (validContacts.length === 0) {
    errors.contacts = 'At least one contact person is required';
  } else {
    const contactErrors = [];
    formData.contacts.forEach((contact, index) => {
      const cErr = {};
      if (contact.name?.trim() && contact.email?.trim() && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(contact.email)) {
        cErr.email = 'Invalid email';
      }
      if (Object.keys(cErr).length > 0) {
        contactErrors[index] = cErr;
      }
    });
    if (contactErrors.length > 0) {
      errors.contactDetails = contactErrors;
    }
  }

  // Phone
  if (formData.phone) {
    const digitsOnly = formData.phone.replace(/\D/g, '');
    const expected = PHONE_LENGTHS[formData.countryCode];
    if (expected && digitsOnly.length !== expected) {
      errors.phone = `Phone number should be ${expected} digits`;
    }
  }

  // Employees (create only)
  if (!isEdit && (!formData.employeeAssignments || formData.employeeAssignments.length === 0)) {
    errors.employees = 'At least one employee must be assigned';
  }

  // PTO settings are now per-employee (configured in PTO Config modal), no client-level validation needed

  return errors;
};

export const getPhoneError = (phone, countryCode) => {
  if (!phone) return '';
  const digitsOnly = phone.replace(/\D/g, '');
  const expected = PHONE_LENGTHS[countryCode];
  if (!expected) return '';
  if (digitsOnly.length > 0 && digitsOnly.length !== expected) {
    return `Phone number should be ${expected} digits`;
  }
  return '';
};

export const getPhoneMaxLength = (countryCode) => {
  return PHONE_LENGTHS[countryCode] || 15;
};
